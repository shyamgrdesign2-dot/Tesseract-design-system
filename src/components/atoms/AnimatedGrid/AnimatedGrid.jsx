const VB = 2500;
const SPEED = 1050;
const CYCLE = 3.0;
const COMET_LEN = 168;
const COMET_T = 2.75;
const LINE_T = 1.65;
const CLIP_T = 3.5;
const INTERCEPT_TOL = 10;

function edgeAsRectD(x1, y1, x2, y2, t) {
  const dx = x2 - x1, dy = y2 - y1;
  const L = Math.hypot(dx, dy);
  if (L === 0) return "";
  const ux = dx / L, uy = dy / L;
  const px = -uy * t / 2, py = ux * t / 2;
  return (
    `M${(x1 + px).toFixed(2)},${(y1 + py).toFixed(2)}` +
    `L${(x2 + px).toFixed(2)},${(y2 + py).toFixed(2)}` +
    `L${(x2 - px).toFixed(2)},${(y2 - py).toFixed(2)}` +
    `L${(x1 - px).toFixed(2)},${(y1 - py).toFixed(2)}Z`
  );
}

const LANES = (() => {
  const tagged = EDGES.map(([x1, y1, x2, y2]) => {
    const dx = x2 - x1, dy = y2 - y1;
    let kind, raw;
    if (Math.abs(dy) < 0.5) { kind = "H"; raw = y1; }
    else if (Math.abs(dx) < 0.5) { kind = "V"; raw = x1; }
    else if (dx * dy < 0) { kind = "/"; raw = y1 + x1; }
    else { kind = "\\"; raw = y1 - x1; }
    return { kind, raw, edge: [x1, y1, x2, y2] };
  });

  const out = [];
  for (const kind of ["H", "V", "/", "\\"]) {
    const items = tagged.filter(t => t.kind === kind).sort((a, b) => a.raw - b.raw);
    if (!items.length) continue;
    let cluster = [items[0]];
    const flush = () => {
      const allPts = cluster.flatMap(c => [
        [c.edge[0], c.edge[1]],
        [c.edge[2], c.edge[3]],
      ]);
      const intercept = cluster.reduce((s, c) => s + c.raw, 0) / cluster.length;
      let sx, sy, ex, ey;
      if (kind === "H") {
        const xs = allPts.map(p => p[0]);
        sx = Math.max(...xs); sy = intercept;
        ex = Math.min(...xs); ey = intercept;
      } else if (kind === "V") {
        const ys = allPts.map(p => p[1]);
        sx = intercept; sy = Math.min(...ys);
        ex = intercept; ey = Math.max(...ys);
      } else if (kind === "/") {
        const ys = allPts.map(p => p[1]);
        const ymin = Math.min(...ys), ymax = Math.max(...ys);
        sx = intercept - ymin; sy = ymin;
        ex = intercept - ymax; ey = ymax;
      } else {
        const ys = allPts.map(p => p[1]);
        const ymin = Math.min(...ys), ymax = Math.max(...ys);
        sx = ymin - intercept; sy = ymin;
        ex = ymax - intercept; ey = ymax;
      }
      const clipD = cluster
        .map(c => edgeAsRectD(c.edge[0], c.edge[1], c.edge[2], c.edge[3], CLIP_T))
        .join("");
      out.push({ kind, intercept, sx, sy, ex, ey, clipD });
    };
    for (let i = 1; i < items.length; i++) {
      if (items[i].raw - cluster[cluster.length - 1].raw > INTERCEPT_TOL) {
        flush();
        cluster = [items[i]];
      } else {
        cluster.push(items[i]);
      }
    }
    flush();
  }

  const orderKey = { H: 0, V: 1, "/": 2, "\\": 3 };
  out.sort((a, b) => {
    if (orderKey[a.kind] !== orderKey[b.kind]) return orderKey[a.kind] - orderKey[b.kind];
    if (a.kind === "H") return a.sy - b.sy;
    if (a.kind === "V") return a.sx - b.sx;
    return a.intercept - b.intercept;
  });
  // Tag each lane with its final, stable index so density-subset rendering can
  // keep clip-path ids consistent regardless of which lanes are dropped.
  out.forEach((lane, i) => { lane.idx = i; });
  return out;
})();

const ALL_EDGES_D = EDGES
  .map(([x1, y1, x2, y2]) => `M${x1},${y1}L${x2},${y2}`)
  .join("");

function LanePulse({ lane, gradId, speed = 1 }) {
  const { sx, sy, ex, ey } = lane;
  const dx = ex - sx, dy = ey - sy;
  const len = Math.hypot(dx, dy);
  const ux = dx / len, uy = dy / len;
  const sxPad = sx - ux * COMET_LEN;
  const syPad = sy - uy * COMET_LEN;
  const exPad = ex + ux * COMET_LEN;
  const eyPad = ey + uy * COMET_LEN;
  const path = `M${sxPad.toFixed(1)},${syPad.toFixed(1)} L${exPad.toFixed(1)},${eyPad.toFixed(1)}`;
  // `speed` scales both the comet velocity and the cycle period inversely, so a
  // higher multiplier means faster comets on a shorter cycle. The active fraction
  // (how long a comet is mid-flight per cycle) is invariant to speed.
  const cycle = CYCLE / speed;
  const travel = (len + 2 * COMET_LEN) / (SPEED * speed);
  const activeFrac = Math.min(travel / cycle, 0.999);

  return (
    <rect
      x={-COMET_LEN / 2}
      y={-COMET_T / 2}
      width={COMET_LEN}
      height={COMET_T}
      fill={`url(#${gradId})`}
    >
      <animateMotion
        dur={`${(CYCLE / speed).toFixed(3)}s`}
        repeatCount="indefinite"
        rotate="auto"
        keyPoints="0;1;1"
        keyTimes={`0;${activeFrac.toFixed(3)};1`}
        calcMode="linear"
        path={path}
      />
    </rect>
  );
}

// Named speed presets → cycle/velocity multipliers (higher = faster).
const SPEED_PRESETS = { slow: 0.6, normal: 1, fast: 1.6 };
const resolveSpeed = (s) => {
  if (typeof s === "number" && Number.isFinite(s) && s > 0) return s;
  if (typeof s === "string" && s in SPEED_PRESETS) return SPEED_PRESETS[s];
  return 1;
};

// React hook: true when the user has asked for reduced motion. SSR-safe (starts
// false, syncs on mount) and updates live if the OS preference changes.
function usePrefersReducedMotion() {
  const [reduced, setReduced] = useState(false);
  useEffect(() => {
    if (typeof window === "undefined" || !window.matchMedia) return undefined;
    const mq = window.matchMedia("(prefers-reduced-motion: reduce)");
    const onChange = () => setReduced(mq.matches);
    onChange();
    mq.addEventListener?.("change", onChange);
    return () => mq.removeEventListener?.("change", onChange);
  }, []);
  return reduced;
}

/**
 * @param {{ className?: string, style?: object,
 *   lineColor?: string,   // lattice stroke (default faint white for dark surfaces)
 *   cometColor?: string,  // travelling pulse color (default white)
 *   edgeFade?: number|boolean, // edge-fade strength 0..1 (true → 0.6, false → 0)
 *   speed?: number|"slow"|"normal"|"fast", // comet speed multiplier (default 1 / "normal")
 *   density?: number,     // 0..1 fraction of lanes that emit comets (default 1 = all)
 *   animated?: boolean,   // false → static lattice, no comets (default true).
 *                         //   prefers-reduced-motion is auto-honored regardless.
 * }} props
 */
export function AnimatedGrid({
  className,
  style,
  lineColor = "color-mix(in srgb, var(--tesseract-slate-0) 14%, transparent)",
  cometColor = "var(--tesseract-slate-0)",
  edgeFade = true,
  speed = "normal",
  density = 1,
  animated = true,
}) {
  const prefersReducedMotion = usePrefersReducedMotion();
  // Render comets only when explicitly animated AND the user hasn't requested
  // reduced motion. When off, just the static lattice draws.
  const showComets = animated && !prefersReducedMotion;
  const speedMul = resolveSpeed(speed);
  // `density` is the FRACTION of lanes that emit comets. 1 → every lane (today's
  // look); lower → an evenly-spread subset (sparser/calmer + cheaper). We pick a
  // deterministic stride across the lane list so the kept lanes stay distributed.
  const dens = Math.max(0, Math.min(1, Number(density)));
  const activeLanes =
    !showComets || dens <= 0
      ? []
      : dens >= 1
        ? LANES
        : LANES.filter((_, i) => Math.floor(i * dens) !== Math.floor((i - 1) * dens));
  // Scope the clip/gradient ids per instance so two differently-themed grids on
  // one page never share (and clobber) each other's definitions.
  const uid = useId().replace(/:/g, "");
  const gradId = `${uid}-comet`;
  // Radial edge fade: lines dissolve toward all four edges + the corners so the
  // grid reads as an ambient texture, never a hard-cropped rectangle. `edgeFade`
  // is the STRENGTH — 0 = no fade, 1 = strong (legacy booleans map: true → 0.6,
  // false → 0). Higher values shrink the solid core and pull the falloff inward.
  // A caller can still override the mask entirely via `style`.
  const fadeAmount = edgeFade === true ? 0.6 : edgeFade === false ? 0 : Math.max(0, Math.min(1, Number(edgeFade) || 0));
  let fadeStyle = null;
  if (fadeAmount > 0) {
    const core = (75 - 55 * fadeAmount).toFixed(0);
    const edge = Math.min(100, 108 - 45 * fadeAmount).toFixed(0);
    const mid = (Number(core) + (Number(edge) - Number(core)) * 0.55).toFixed(0);
    const fadeMask = `radial-gradient(closest-side, #000 ${core}%, rgba(0,0,0,0.3) ${mid}%, transparent ${edge}%)`;
    fadeStyle = { WebkitMaskImage: fadeMask, maskImage: fadeMask };
  }
  return (
    <svg
      className={className}
      style={{ ...fadeStyle, ...style }}
      viewBox={`0 0 ${VB} ${VB}`}
      xmlns="http://www.w3.org/2000/svg"
      fill="none"
      preserveAspectRatio="xMidYMid slice"
      aria-hidden
    >
      <defs>
        {activeLanes.map((lane) => (
          <clipPath key={lane.idx} id={`${uid}-lc${lane.idx}`} clipPathUnits="userSpaceOnUse">
            <path d={lane.clipD} />
          </clipPath>
        ))}

        <linearGradient id={gradId} x1="0" y1="0" x2="1" y2="0">
          <stop offset="0" stopColor={cometColor} stopOpacity="0" />
          <stop offset="0.55" stopColor={cometColor} stopOpacity="0.5" />
          <stop offset="0.9" stopColor={cometColor} stopOpacity="0.95" />
          <stop offset="1" stopColor={cometColor} stopOpacity="1" />
        </linearGradient>
      </defs>

      <g>
        <path
          d={ALL_EDGES_D}
          stroke={lineColor}
          strokeWidth={LINE_T}
          strokeLinecap="square"
          fill="none"
        />

        {activeLanes.map((lane) => (
          <g key={lane.idx} clipPath={`url(#${uid}-lc${lane.idx})`}>
            <LanePulse lane={lane} gradId={gradId} speed={speedMul} />
          </g>
        ))}
      </g>
    </svg>
  );
}

export default AnimatedGrid;
